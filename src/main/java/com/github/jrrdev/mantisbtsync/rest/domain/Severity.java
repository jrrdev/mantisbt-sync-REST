package com.github.jrrdev.mantisbtsync.rest.domain;

import java.util.List;

import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.OneToMany;
import javax.persistence.Table;

/**
 * Domain object stored into mantis_enum_severities.
 *
 * @author jrrdev
 *
 */
@Entity
@Table(name = "mantis_enum_severities")
public class Severity {

	@Id
	@GeneratedValue(strategy = GenerationType.AUTO)
	private long id;

	private String name;

	@OneToMany(mappedBy="severity", fetch = FetchType.LAZY)
	private List<Bug> bugs;

	/**
	 * @return the id
	 */
	public long getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(final long id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(final String name) {
		this.name = name;
	}

	/**
	 * @return the bugs
	 */
	public List<Bug> getBugs() {
		return bugs;
	}

	/**
	 * @param bugs the bugs to set
	 */
	public void setBugs(final List<Bug> bugs) {
		this.bugs = bugs;
	}

}
